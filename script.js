/* ‚úÖ –ü–µ—Ä–µ–±–∏—Ä–∞—î–º–æ —Å–ø–∏—Å–æ–∫ —Ñ—Ä—É–∫—Ç—ñ–≤ –∑–∞ –¥–æ–ø–æ–º–æ–≥–æ—é forEach() */
